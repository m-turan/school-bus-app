package com.murat.servis.driver;

public class Driver {

    private String driver_name;
    private String driver_surname;
    private String driver_mail;
    private String driver_phone;
    private String driver_password;
    private String driver_birthday;

    public String getDriver_name() {
        return driver_name;
    }

    public void setDriver_name(String driver_name) {
        this.driver_name = driver_name;
    }

    public String getDriver_surname() {
        return driver_surname;
    }

    public void setDriver_surname(String driver_surname) {
        this.driver_surname = driver_surname;
    }

    public String getDriver_mail() {
        return driver_mail;
    }

    public void setDriver_mail(String driver_mail) {
        this.driver_mail = driver_mail;
    }

    public String getDriver_phone() {
        return driver_phone;
    }

    public void setDriver_phone(String driver_phone) {
        this.driver_phone = driver_phone;
    }

    public String getDriver_password() {
        return driver_password;
    }

    public void setDriver_password(String driver_password) {
        this.driver_password = driver_password;
    }

    public String getDriver_birthday() {
        return driver_birthday;
    }

    public void setDriver_birthday(String driver_birthday) {
        this.driver_birthday = driver_birthday;
    }
}
