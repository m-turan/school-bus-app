package com.murat.servis.parent;

public class Parent {
    private String parent_name;
    private String parent_surname;
    private String parent_phone;
    private String parent_mail;
    private String parent_password;
    private String birthday;

    public String getParent_name() {
        return parent_name;
    }

    public void setParent_name(String parent_name) {
        this.parent_name = parent_name;
    }

    public String getParent_surname() {
        return parent_surname;
    }

    public void setParent_surname(String parent_surname) {
        this.parent_surname = parent_surname;
    }

    public String getParent_phone() {
        return parent_phone;
    }

    public void setParent_phone(String parent_phone) {
        this.parent_phone = parent_phone;
    }

    public String getParent_mail() {
        return parent_mail;
    }

    public void setParent_mail(String parent_mail) {
        this.parent_mail = parent_mail;
    }

    public String getParent_password() {
        return parent_password;
    }

    public void setParent_password(String parent_password) {
        this.parent_password = parent_password;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }
}
